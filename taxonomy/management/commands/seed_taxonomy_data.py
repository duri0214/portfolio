class Command(BaseCommand):
    help = "Taxonomyの初期データと追加動物分類候補をまとめて投入します"

    @transaction.atomic
    def handle(self, *args, **options):
        initial_count = self._seed_initial_data()
        animal_count = self._seed_animal_taxonomy()
        self.stdout.write(
            self.style.SUCCESS(
                "taxonomy data seed completed: "
                f"{initial_count} initial records, {animal_count} animal breeds"
            )
        )

    def _seed_initial_data(self):
        saved_count = 0

        for row in INITIAL_TAXONOMY_SEED:
            model = apps.get_model(row["model"])
            fields = row["fields"].copy()
            pk = row.get("pk") or fields.pop("id", None)

            defaults = self._build_defaults(model, fields)
            model.objects.update_or_create(pk=pk, defaults=defaults)
            saved_count += 1

        return saved_count

    def _build_defaults(self, model, fields):
        defaults = {}

        for field in model._meta.fields:
            if field.primary_key:
                continue

            if field.name in fields:
                value = fields[field.name]
            elif field.attname in fields:
                value = fields[field.attname]
            else:
                continue

            if field.is_relation:
                defaults[field.attname] = value
            else:
                defaults[field.name] = value

        return defaults

    def _seed_animal_taxonomy(self):
        created_count = 0

        for row in ANIMAL_TAXONOMY_SEED:
            kingdom = self._get_or_create(Kingdom, row["kingdom"])
            phylum = self._get_or_create(Phylum, row["phylum"], kingdom=kingdom)
            classification = self._get_or_create(
                Classification, row["classification"], phylum=phylum
            )
            family = self._get_or_create(
                Family, row["family"], classification=classification
            )
            genus = self._get_or_create(Genus, row["genus"], family=family)
            species = self._get_or_create(Species, row["species"], genus=genus)

            for name, name_kana, remark in row["breeds"]:
                _, created = Breed.objects.get_or_create(
                    species=species,
                    name=name,
                    defaults={
                        "name_kana": name_kana,
                        "remark": remark,
                        "natural_monument": None,
                    },
                )
                created_count += int(created)

        return created_count

    def _get_or_create(self, model, data: tuple[str, ...], **parents):
        name, name_en, *remarks = data
        obj, _ = model.objects.get_or_create(
            **parents,
            name=name,
            defaults={"name_en": name_en, "remark": remarks[0] if remarks else None},
        )
        return obj
